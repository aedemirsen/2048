    private void geriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_geriActionPerformed
        Methods_2048 m = new Methods_2048();
        if (!geriListTutucu.isEmpty()) {
            for (int i = 0; i < label.length; i++) {
                for (int j = 0; j < label[i].length; j++) {
                    label[i][j].setText(geriListTutucu.get(geriListTutucu.size() - 1)[i][j]);
                }
            }
            geriListTutucu.remove(geriListTutucu.size() - 1);
        }
        m.renkleriBelirle(panelList, labelList);
    }//GEN-LAST:event_geriActionPerformed

