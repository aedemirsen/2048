    private void puanKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_puanKeyPressed
        // TODO add your handling code here:
        Methods_2048 m = new Methods_2048();  
        String[][] geriList = new String[4][4];
        for (int i = 0; i < label.length; i++) {
            for (int j = 0; j < label[i].length; j++) {
                geriList[i][j] = label[i][j].getText();
            }
        }
       
            geriListTutucu.add(geriList);
        
        if (evt.getKeyCode() == KeyEvent.VK_UP) {
            m.yukari(label,panelList,labelList);            
        } 
        else if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
           m.asagi(label,panelList,labelList);  
        }
        else if (evt.getKeyCode() == KeyEvent.VK_RIGHT) {
           m.saga(label,panelList,labelList); 
        }
        else if (evt.getKeyCode() == KeyEvent.VK_LEFT) {
           m.sola(label,panelList,labelList);  
        }
        if (Integer.parseInt(puan.getText()) > Integer.parseInt(best.getText())) {
            _2048.best.setText(_2048.puan.getText());
            m.rekoruYaz(_2048.best.getText());
        }
        jDialog1.setVisible(m.bittiMi(label));  
        
    }//GEN-LAST:event_puanKeyPressed

